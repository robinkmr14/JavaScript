// Math Object 


// round()
let number = 7.5;
console.log(Math.round(number)); // round to nearest integer

// floor() -->  round off to down
console.log(Math.floor(number));

// ceil() --> round off to up 
console.log(Math.ceil(number));

//trunc() --> remove decimal part
console.log(Math.trunc(number));

// random() --> generate autometic number in the form of decimal
console.log(Math.random(number));